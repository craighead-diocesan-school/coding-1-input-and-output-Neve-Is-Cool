function madLibs() {
  let exclamation = prompt ('Give me an exclamation...')
  let adverb = prompt ('Give me an adverb...')
  let noun = prompt ('Give me a noun...')
  let adjective = prompt ('Give me an adjective...')

  alert (exclamation + '! he said ' + adverb + ' as he jumped into his brand new ' + noun + ' and drove of with his ' + adjective + ' wife.' )
}